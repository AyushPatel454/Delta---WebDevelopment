let num = 3;

switch(num) {
    case 1:
        console.log("Monday - 1");
        break;
    case 2:
        console.log("Tuesday - 2");
        break;
    case 3:
        console.log("Wednesday - 3");
        break;
    case 4:
        console.log("Thursday - 4");
        break;
    case 5:
        console.log("Friday - 5");
        break;
    case 6:
        console.log("Saturday - 6");
        break;
    default:
        console.log("Sunday - 7");
}